package week02.operator;

public class w09 {
    public static void main(String[] args) {
        short x = 10;
        int y = 20;

        int z = x + y; //int

        long lx = 30L;
        long lz = z + lx; //long

        float fx = x; //float
        float fy = y; //float
        float fz = z; //float
        System.out.println(lz);
        System.out.println(fx);
        System.out.println(fy);
        System.out.println(fz);
    }
}
