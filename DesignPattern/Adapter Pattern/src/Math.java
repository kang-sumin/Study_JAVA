//이미 구현된(주어진) 함수 클래스
public class Math {

    //두 배의 수를 구하는 함수
    public static double towTime(double num){
        return num*2;
    }

    //절반을 구하는 함수
    public static double half(double num){
        return num/2;
    }

    //강화된 두배를 구하는 함수 알고리즘 (새롭게 추가됨/같지만 바뀌었다고 가정함)
    public static Double doubled(Double d){
        return d*2;
    }

}
